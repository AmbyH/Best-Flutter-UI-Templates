  static const int maxCarbs = 300;
  static const int maxProtein = 50;
  static const int maxFat = 78;

  @override
  void initState() {
    super.initState();
    _loadSavedValues();
  }

  @override
  void didUpdateWidget(covariant MediterranesnDietView oldWidget) {
    super.didUpdateWidget(oldWidget);
    if (widget.date != oldWidget.date) {
      _loadSavedValues();
    }
