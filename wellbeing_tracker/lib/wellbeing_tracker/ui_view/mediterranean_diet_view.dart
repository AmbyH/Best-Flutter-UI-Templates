  Future<void> _loadSavedValues() async {
    DateTime date = widget.date ?? DateTime.now();
    UserDailyData data = await UserDailyData.load(date: date);
    setState(() {
      eatenCalories = data.caloriesEaten;
      burnedCalories = data.caloriesBurned;
      // Calculate remainingCalories as dailyGoal - eatenCalories + burnedCalories
      remainingCalories = dailyGoal - eatenCalories + burnedCalories;
      carbs = data.carbs;
      protein = data.protein;
      fat = data.fat;
    });
  }

  double get totalBurnedCalories => burnedCalories.toDouble();

  double get remainingPercent {
    if (dailyGoal == 0) return 0;
    // Flip the percent so the wheel fills as you eat more, and clamp to [0, 1]
    final percent = 1.0 - (remainingCalories / dailyGoal);
    return percent.clamp(0.0, 1.0);
  }

